namespace Rock.Cryptography
{
    /// <summary>
    /// Simple Enum used with the String Extension Method ToHash()
    /// </summary>
    public enum HashType
    {
        /// <summary>
        /// MD5 Hash Type
        /// </summary>
        MD5,
        /// <summary>
        /// SHA1 Hash Type
        /// </summary>
        SHA1,
        /// <summary>
        /// SHA256 Hash Type
        /// </summary>
        SHA256,
        /// <summary>
        /// SHA512 Hash Type
        /// </summary>
        SHA512
    }
}